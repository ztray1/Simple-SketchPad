
package data;

public class ColorParam {

	private int colR;
	private int colG;
	private int colB;

	public ColorParam(int colR, int colG, int colB) {
		super();
		this.colR = colR;
		this.colG = colG;
		this.colB = colB;
	}

	public int getColR() {
		return colR;
	}

	public void setColR(int colR) {
		this.colR = colR;
	}

	public int getColG() {
		return colG;
	}

	public void setColG(int colG) {
		this.colG = colG;
	}

	public int getColB() {
		return colB;
	}

	public void setColB(int colB) {
		this.colB = colB;
	}
}
